// Первое задание

function hasEvenNumber(arr) {
  let foundEven = false;

  for (let i = 0; i < arr.length; i++) {
    if (arr[i] % 2 === 0) {
      foundEven = true;
    }
  }

  return foundEven;
}

console.log(hasEvenNumber([1, 3, 4, 5])); // Ожидается: true

